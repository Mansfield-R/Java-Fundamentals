package com.section6.CompositionLesson.Challenge_House;

public class Dimensions {

    private int width;
    private int length;

    public Dimensions(int width, int length) {
        this.width = width;
        this.length = length;
    }

    public int getWidth() {
        return width;
    }

    public int getHeight() {
        return length;
    }
}
